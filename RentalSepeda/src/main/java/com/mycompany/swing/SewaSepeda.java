    private void btnPilihSpdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihSpdActionPerformed
        // TODO add your handling code here:
        String jenisSepeda;
        int jumlahSepeda, hargaSewa;
        sepedaService = new SepedaServiceImpl();
        
        jenisSepeda = cbJenisSpd.getSelectedItem().toString();
        jumlahSepeda = Integer.parseInt(txtJumlahSpd.getText());
        hargaSewa = Integer.parseInt(txtHargaSewa.getText());
        
        Sepeda sepeda = new Sepeda();
        sepeda.setJenisSepeda(jenisSepeda);
        sepeda.setJumlahSepeda(jumlahSepeda);
        sepeda.setHargaSewa(hargaSewa);
        
        String tanggalOrder;
        int durasiSewa, jumlahSewa;
        Date date = txtTanggal.getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        tanggalOrder = dateFormat.format(date);
        durasiSewa = Integer.parseInt(txtDurasi.getText());
        jumlahSewa = Integer.parseInt(txtJumlahSewa.getText());
        
        Transaksi transaksi = new Transaksi();
        transaksi.setTanggalOrder(tanggalOrder);
        transaksi.setDurasiSewa(durasiSewa);
        transaksi.setJumlahSewa(jumlahSewa);
        
        int totalHarga;
        if (durasiSewa >= 60) {
        totalHarga = hargaSewa * (durasiSewa / 60) * jumlahSewa;
        } else {
        totalHarga = (hargaSewa * jumlahSewa);
        }
        String byr = Integer.toString(totalHarga);
        txtTotalHarga.setText(byr);
    }//GEN-LAST:event_btnPilihSpdActionPerformed

