    private void btnSewaSpdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSewaSpdActionPerformed
        // TODO add your handling code here:
        String jenisSepeda;
        int jumlahSepeda, hargaSewa, idSepeda, durasiSewa, jumlahSewa, sisaJumlahSepeda;
        String tanggalOrder;
        transaksiService = new TransaksiServiceImpl();
      
        Date date = txtTanggal.getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        tanggalOrder = dateFormat.format(date);
        jumlahSewa = Integer.parseInt(txtJumlahSewa.getText());
        durasiSewa = Integer.parseInt(txtDurasi.getText());
        idSepeda = Integer.parseInt(txtIdSpd.getText());
        jenisSepeda = cbJenisSpd.getSelectedItem().toString();
        hargaSewa = Integer.parseInt(txtHargaSewa.getText());
        jumlahSepeda = Integer.parseInt(txtJumlahSpd.getText());
        sisaJumlahSepeda = (jumlahSepeda - jumlahSewa);
        
        Transaksi transaksi = new Transaksi();
        transaksi.setTanggalOrder(tanggalOrder);
        transaksi.setJumlahSewa(jumlahSewa);
        transaksi.setDurasiSewa(durasiSewa);
        transaksi.setIdSepeda(idSepeda);
        transaksi.setJenisSpd(jenisSepeda);
        transaksi.setHargaSewa(hargaSewa);
        
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Anda yakin untuk melakukan Transaksi?", "Warning", dialogButton);

        if (dialogResult == JOptionPane.YES_OPTION) {
            transaksiService.create(transaksi);
            JOptionPane.showMessageDialog(null, "Data transaksi berhasil ditambahkan!");
            loadData();
            emptyField();
        }
    }//GEN-LAST:event_btnSewaSpdActionPerformed

