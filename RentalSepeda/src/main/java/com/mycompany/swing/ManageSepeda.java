    private void loadData() {
        sepedaService = new SepedaServiceImpl();
        List<Sepeda> listSepeda;
        listSepeda = sepedaService.findAll();
        Object[][] objectSepeda = new Object[listSepeda.size()][4];
        
        int counter = 0;
        
        for (Sepeda sepeda : listSepeda) {
            objectSepeda[counter][0] = sepeda.getIdSepeda();
            objectSepeda[counter][1] = sepeda.getJenisSepeda();
            objectSepeda[counter][2] = sepeda.getJumlahSepeda();
            objectSepeda[counter][3] = sepeda.getHargaSewa();
            counter++;
        }
        
        tblSepeda.setModel(new javax.swing.table.DefaultTableModel(
                objectSepeda,
                new String [] {
                    "id_spd", "jenis_spd", "jumlah_sepeda", "harga_sewa"
                }
        ));
    }
    
    private void loadData(Sepeda sepeda) {
        Object[][] objectSepeda = new Object[1][4];
        
        objectSepeda[0][0] = sepeda.getIdSepeda();
        objectSepeda[0][1] = sepeda.getJenisSepeda();
        objectSepeda[0][2] = sepeda.getJumlahSepeda();
        objectSepeda[0][3] = sepeda.getHargaSewa();
        
        tblSepeda.setModel(new javax.swing.table.DefaultTableModel(
                objectSepeda,
                new String [] {
                    "id_spd", "jenis_spd", "jumlah_sepeda", "harga_sewa"
                }
        ));
    }
    
    private Sepeda findSepeda(int id) {
        Sepeda sepeda = new Sepeda();
        sepedaService = new SepedaServiceImpl();
        sepeda = sepedaService.findById(id);
        return sepeda;
    }

