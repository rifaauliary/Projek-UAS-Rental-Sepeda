    private void tblSepedaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSepedaMouseClicked
        // TODO add your handling code here:
        String jenisSepeda;
        int jumlahSepeda, hargaSewa, idSepeda;
        
        int row = tblSepeda.getSelectedRow();
        idSepeda = Integer.parseInt(tblSepeda.getValueAt(row, 0).toString());
        jenisSepeda = tblSepeda.getValueAt(row, 1).toString();
        jumlahSepeda = Integer.parseInt(tblSepeda.getValueAt(row, 2).toString());
        hargaSewa = Integer.parseInt(tblSepeda.getValueAt(row, 3).toString());
        
        txtId.setText(idSepeda+"");
        cbJenisSpd.setSelectedItem(jenisSepeda);
        txtJumlh.setText(jumlahSepeda+"");
        txtharga.setText(hargaSewa+"");
    }//GEN-LAST:event_tblSepedaMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int id;
        sepedaService = new SepedaServiceImpl();
        int dialogButton = JOptionPane.YES_NO_OPTION;
        
        id = Integer.parseInt(txtId.getText());
        
        int dialogResult = JOptionPane.showConfirmDialog (null, "Are you sure to delete it?", "Warning", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION){
            sepedaService.delete(id);
            loadData();
            emptyField();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        int id;
        Sepeda searchedSepeda = new Sepeda();
        
        id = Integer.parseInt(txtSearch.getText());
        searchedSepeda = findSepeda(id);
        if (searchedSepeda != null) {
            loadData(searchedSepeda);
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan!");
        }
    }//GEN-LAST:event_btnCariActionPerformed

