    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        // TODO add your handling code here:
        String nik, namaCustomer, tanggalLahir, gender, nomorTelp, alamat;
        int id;
        Date setTanggalLahir;
        
        int row = tblCustomer.getSelectedRow();
        id = Integer.parseInt(tblCustomer.getValueAt(row, 0).toString());
        nik = tblCustomer.getValueAt(row, 1).toString();
        namaCustomer = tblCustomer.getValueAt(row, 2).toString();
        tanggalLahir = tblCustomer.getValueAt(row, 3).toString();
        gender = tblCustomer.getValueAt(row, 4).toString();
        nomorTelp = tblCustomer.getValueAt(row, 5).toString();
        alamat = tblCustomer.getValueAt(row, 6).toString();
        
        txtId.setText(id+"");
        txtNik.setText(nik);
        txtNama.setText(namaCustomer);
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            setTanggalLahir = dateFormat.parse(tanggalLahir);
            txtTanggalLahir.setDate(setTanggalLahir);
        } catch (ParseException ex) {
            Logger.getLogger(ManageTransaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (gender.equals("Laki Laki")) {
            rLakiLaki.setSelected(true);
        }
        if (gender.equals("Perempuan")) {
            rPerempuan.setSelected(true);
        }
        txtNotelp.setText(nomorTelp);
        txtAlamat.setText(alamat);
    }//GEN-LAST:event_tblCustomerMouseClicked

