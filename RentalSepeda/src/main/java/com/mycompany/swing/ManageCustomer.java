    private void loadData() {
        customerService = new CustomerServiceImpl();
        List<Customer> listCustomer;
        listCustomer = customerService.findAll();
        Object[][] objectCustomer = new Object[listCustomer.size()][7];
        
        int counter = 0;
        for (Customer customer : listCustomer) {
            objectCustomer[counter][0] = customer.getIdCustomer();
            objectCustomer[counter][1] = customer.getNik();
            objectCustomer[counter][2] = customer.getNamaCustomer();
            objectCustomer[counter][3] = customer.getTanggalLahir();
            objectCustomer[counter][4] = customer.getJenisKelamin();
            objectCustomer[counter][5] = customer.getNomorTelepon();
            objectCustomer[counter][6] = customer.getAlamat();
            counter++;
        }
        
        tblCustomer.setModel(new javax.swing.table.DefaultTableModel(
                objectCustomer,
                new String [] {
                    "id_cust", "nik", "nama_cust", "tgl_lhr", "gender", "nomor_telp", "alamat"
                }
        ));
    }
    
